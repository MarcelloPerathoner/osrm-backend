"""Benchmark to compare two osrm-routed binaries and/or two datasets.

This program lets you compare two osrm-routed binaries and/or two differently generated
datasets. All datasets must have the same extent. You also must provide a CSV file
containing points inside the dataset.

The run subcommand will select the first binary and/or dataset and request routes
between N pairs of randomly selected points.  It will then select the next binary and/or
dataset and request the same routes. The whole process will be repeated M times.

The report subcommand will analyze the logfile generated by the run subcommand and
produce a report.

"""

import argparse
import csv
import datetime
import itertools
import random
import re
import requests
import subprocess

import pandas as pd
import tabulate


class Client:
    def __init__(self, args):
        self.host = args.host
        self.url = f"{self.host}/route/v1/driving"

        self.coordinates = []
        """ list of (lon, lat) """
        with open(args.coordinates, newline="") as csvfile:
            reader = csv.DictReader(csvfile, delimiter="\t")
            for row in reader:
                print(row)
                self.coordinates.append((float(row["lat"]), float(row["lon"])))

    def make_url(self):
        def toString(coords) -> str:
            return ";".join([f"{coord[1]:.6f},{coord[0]:.6f}" for coord in coords])

        coords = random.sample(self.coordinates, 2)
        return f"{self.url}/{toString(coords)}?overview=full&steps=true"

    def make_request(self):
        """Make one routed request"""
        requests.get(self.make_url())


def run(args):
    with open(args.logfile, "w") as log:

        def tee(text):
            if text != ".\n":
                print(text, end="")
                log.write(text)
                log.flush()

        client = Client(args)

        # alternates runs of each binary / dataset
        for n, binary, dataset in itertools.product(
            range(0, args.runs), args.binaries, args.datasets
        ):
            proc = subprocess.Popen(
                [binary, "-a", "CH", dataset],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                text=True,
            )
            tee(f'### {n} "{binary}" "{dataset}"\n')
            left = args.run_length
            ready = False
            random.seed(69)
            while left > 0 and proc.poll() is None:
                if ready:
                    client.make_request()
                    left -= 1
                text = proc.stdout.readline()
                if not ready and "running" in text:
                    ready = True
                tee(text)
            proc.kill()


def report(args):
    rows = list()
    index = list()
    with open(args.logfile) as log:
        for line in log:
            if m := re.search(r"^### (\d+) \"(.*?)\" \"(.*?)\"$", line):
                index.append((int(m.group(1)), m.group(2), m.group(3)))
                rows.append({"samples": 0, "time": 0.0})
            if m := re.search(r"([.\d]+)ms", line):
                rows[-1]["time"] += float(m.group(1))
                rows[-1]["samples"] += 1

    df = pd.DataFrame(
        rows, index=pd.MultiIndex.from_tuples(index, names=("run", "binary", "dataset"))
    )

    print(f"## RAW data - {datetime.datetime.now().isoformat()}\n```")
    print(df)
    print("```")

    groupby = ["binary", "dataset"]

    print("\n## Summary\n")
    grouped = df.groupby(groupby)

    agg = grouped.time.aggregate(["mean", "std"])
    agg = agg.assign(norm=pd.Series(agg["mean"] / agg["mean"].iloc[0]).values)

    agg = agg.reset_index(names=groupby)

    headers = ("binary", "dataset", "mean (ms)", "stdev", "norm")
    floatfmt = ("", "", ".2f", ".2f", ".3f")
    print(
        tabulate.tabulate(
            agg, headers, tablefmt="github", floatfmt=floatfmt, showindex=False
        )
    )


def build_parser():
    parser = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter
    )
    subparsers = parser.add_subparsers()

    parser_run = subparsers.add_parser("run", help="run the benchmark")
    parser_run.set_defaults(func=run)
    parser_run.add_argument(
        "--binaries",
        nargs="+",
        metavar="PATH",
        help="The osrm-routed binaries to use or compare (path/to/osrm-routed)",
    )
    parser_run.add_argument(
        "--datasets",
        nargs="+",
        metavar="PATH",
        help="Datasets to use or compare (path/to/dataset.osrm)",
    )
    parser_run.add_argument(
        "--coordinates",
        required=True,
        metavar="PATH",
        help="A CSV-file of coordinates with the columns 'lat' and 'lon'",
    )
    parser_run.add_argument(
        "--logfile",
        required=True,
        metavar="PATH",
        help="where to write the test log",
    )
    parser_run.add_argument(
        "--runs", help="How many times to run the comparison", default=10, metavar="NUM"
    )
    parser_run.add_argument(
        "--run-length",
        help="How many requests to make in each run",
        default=1000,
        metavar="NUM",
    )
    parser_run.add_argument(
        "--host",
        type=str,
        # "localhost" has abysmal performance on Windows
        default="http://127.0.0.1:5000",
        metavar="URL:PORT",
        help="Host URL (http://127.0.0.1:5000)",
    )

    parser_report = subparsers.add_parser(
        "report", help="analyze the benchmark logfile"
    )
    parser_report.set_defaults(func=report)
    parser_report.add_argument(
        "--logfile",
        help="the log file to process",
        required=True,
        metavar="PATH",
    )

    return parser


def main():
    args = build_parser().parse_args()
    args.func(args)


if __name__ == "__main__":
    main()
